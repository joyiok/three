import type { CellKind, EnemyKind, LevelDef, Vec, WaveDef, WaveGroup } from './types';

function g(
  kind: EnemyKind,
  count: number,
  interval: number,
  delay = 0,
  pathIndex?: number,
): WaveGroup {
  return pathIndex === undefined
    ? { kind, count, interval, delay }
    : { kind, count, interval, delay, pathIndex };
}

function w(...groups: WaveGroup[]): WaveDef {
  return { groups };
}

/** 从起点沿一串拐点展开为逐格路点 */
function expand(...corners: [number, number][]): Vec[] {
  const out: Vec[] = [];
  let [x, y] = corners[0];
  out.push({ x, y });
  for (let i = 1; i < corners.length; i++) {
    const [tx, ty] = corners[i];
    while (x !== tx || y !== ty) {
      x += Math.sign(tx - x);
      y += Math.sign(ty - y);
      out.push({ x, y });
    }
  }
  return out;
}

export function cellKind(level: LevelDef, x: number, y: number): CellKind {
  const c = level.grid[y]?.[x];
  return c === '#' ? 'path' : c === '.' ? 'grass' : 'rock';
}

